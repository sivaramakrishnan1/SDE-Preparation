class RowIncreaseRectangle {
    public static void main(String[] args) {
        
        int val = 5;

        for(int i = 1 ; i <= val ; i++)
        {
            for(int j = 1 ; j <= val ; j++)
            {
                System.out.print(i + " ");
            }
            System.out.println();
        }
    }
}
